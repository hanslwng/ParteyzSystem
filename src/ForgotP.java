    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     String un = txtE.getText();
        String secq = txtBirth1.getText(); 

        String dbUrl = "jdbc:mysql://127.0.0.1:3306/reg";
        String dbUser = "root";
        String dbPassword = "";

        try {
            // Load the MySQL JDBC driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Create a connection to the MySQL database
            try (Connection con = DriverManager.getConnection(dbUrl, dbUser, dbPassword)) {
                
                // Validate input fields
                if (un.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Email required");
                    return;
                }
                if (secq.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "BirthdaySecurityQuestion answer required");
                    return;
                }

                String query = "SELECT * FROM user WHERE Email = ? AND secque = ?";
                try (PreparedStatement pst = con.prepareStatement(query)) {
                    pst.setString(1, un);
                    pst.setString(2, secq);
                    try (ResultSet rs = pst.executeQuery()) {
                        if (rs.next()) {
                             JOptionPane.showMessageDialog(null, "User Confirmed");
                            ForgotPass non = new ForgotPass(un);
                            non.setVisible(true);
                            
                            this.dispose(); 
                        } else {
                            // If user not found, display error message
                            JOptionPane.showMessageDialog(null, "Incorrect Email or Security Question Answer");
                        }
                    }
                }
            }
        } catch (SQLException ex) {
            // Handle SQL exception
            Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            // Handle exception for JDBC driver not found
            Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "JDBC Driver not found: " + ex.getMessage());
        } catch (Exception ex) {
            // Handle other exceptions
            Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "An unexpected error occurred: " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

