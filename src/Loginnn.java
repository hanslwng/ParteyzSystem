    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String un = txtE.getText();
        String ps = txtPass.getText();

       String dbUrl = "jdbc:mysql://127.0.0.1:3306/reg";
        String dbUser = "root";
        String dbPassword = "";
        try {
            // Load the MySQL JDBC driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Create a connection to the MySQL database
            Connection con = DriverManager.getConnection(dbUrl, dbUser, dbPassword);

            String sql = "SELECT * FROM user WHERE Email=? AND Password=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, un);
            pst.setString(2, ps);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(new JFrame(), "LOGIN SUCCESSFULLY!!");
                new Home().setVisible(true);
                new LAW().setVisible(true);
                // Assuming `this` refers to a JFrame, call dispose to close the current window
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(new JFrame(), "Incorrect Email or Password\nPlease try again.", "Warning", JOptionPane.WARNING_MESSAGE);
                txtPass.setText("");
            }

            rs.close();
            pst.close();
            con.close();
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

