    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   String NP = NewP.getText();
        String PC = ConP.getText();

        if (NP.equals("") || PC.equals("")) {
            JOptionPane.showMessageDialog(this, "Passwords cannot be empty!");
        } else if (NP.equals(PC)) {
            String dbUrl = "jdbc:mysql://127.0.0.1:3306/reg";
        String dbUser = "root";
        String dbPassword = "";

            try {
                // Load the MySQL JDBC driver
                Class.forName("com.mysql.cj.jdbc.Driver");

                // Establish connection to MySQL database
                try (Connection con = DriverManager.getConnection(dbUrl, dbUser, dbPassword)) {
                    // Prepare and execute SQL update query
                    String updateQuery = "UPDATE `user` SET Password = ? WHERE Email = ?";
                    try (PreparedStatement pst = con.prepareStatement(updateQuery)) {
                        pst.setString(1, NP); // Set new password
                        pst.setString(2, un); // Set email
                        int rowsAffected = pst.executeUpdate();
                        if (rowsAffected > 0) {
                            JOptionPane.showMessageDialog(null, "New Password Has Been Saved");
                            // Navigate to the login page
                            Loginnn page = new Loginnn();
                            page.setVisible(true);
                            this.dispose();
                        } else {
                            JOptionPane.showMessageDialog(this, "Failed to update password!");
                        }
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Failed to update password! Please try again later.");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ForgotP.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "MySQL Driver not found! Please ensure it's included in your library.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

